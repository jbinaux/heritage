package School;


/**
 * 
 */
public class Courses {

    /**
     * Default constructor
     */
    public Courses() {
    }

    /**
     * 
     */
    public String name;

    /**
     * 
     */
    public int[] grades;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int[] getGrades() {
		return grades;
	}

	public void setGrades(int[] grades) {
		this.grades = grades;
	}

}